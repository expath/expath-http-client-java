# resolve links - $0 may be a softlink
PRG="$0"

while [ -h "$PRG" ]; do
  ls=`ls -ld "$PRG"`
  link=`expr "$ls" : '.*-> \(.*\)$'`
  if expr "$link" : '/.*' > /dev/null; then
    PRG="$link"
  else
    PRG=`dirname "$PRG"`/"$link"
  fi
done

PRGDIR=`dirname "$PRG"`
BASEDIR=`cd "$PRGDIR/.." >/dev/null; pwd`

EXAMPLE_DIR="$BASEDIR/examples/example1"

# show the command that will be executed below
set -x

exec $PRGDIR/transform.sh -config:$EXAMPLE_DIR/config.xml -s:$EXAMPLE_DIR/input1.xml -xsl:$EXAMPLE_DIR/example1.xslt "$@"
